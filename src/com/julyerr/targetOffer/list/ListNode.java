package com.julyerr.targetOffer.list;

public class ListNode {
    public Integer data;
    public ListNode next;

    public ListNode(Integer data) {
        this.data = data;
    }

    public ListNode(Integer data, ListNode next) {
        this.data = data;
        this.next = next;
    }

    public ListNode() {
    }
}
